public class task2_2{
    public static void main(String[] args) {

        double mark1 = 78.6;
        double mark2 = 85.2;
        System.out.println("Maximum Marks: " + Math.max(mark1, mark2));
        System.out.println("Minimum Marks: " + Math.min(mark1, mark2));
        System.out.println("Rounded Marks: " + Math.round(mark1));
        System.out.println("Square: " + Math.pow(mark1, 2));
        System.out.println("Square Root: " + Math.sqrt(mark1));
        System.out.println("Absolute Difference: " + Math.abs(mark1 - mark2));
        System.out.println("Ceiling: " + Math.ceil(mark1));
        System.out.println("Floor: " + Math.floor(mark1));
        System.out.println("Random Number: " + (Math.random() * 100));
        System.out.println("Exponential value: " + Math.exp(2));
        System.out.println("Log value: " + Math.log(10));
        System.out.println("Sin value: " + Math.sin(30));
        System.out.println("Cos value: " + Math.cos(30));
        int num = 4;
        System.out.println("Cube: " + Math.pow(num, 3));
    }
}